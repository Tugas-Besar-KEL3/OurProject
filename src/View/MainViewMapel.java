    private void buttonAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonAddActionPerformed
        // TODO add your handling code here:
        String kodemapel = txtKodeMapel.getText();
        Integer idpengajar = Integer.parseInt(cbIdPengajar.getSelectedItem().toString());
        String mapel = txtMapel.getText();
        String pkeahlian = txtJurusan.getText();
        String kurikulum = txtKurikulum.getText();
        try {
            Statement statement = (Statement) medikacomDatabase.getConnection().createStatement();
            statement.executeUpdate("insert into data_mapel values ('" + kodemapel + "','" + idpengajar + "','" + mapel + "','" + pkeahlian + "','" + kurikulum + "');");
            statement.close();
            JOptionPane.showMessageDialog(null, "Data berhasil disimpan!");
        } catch (Throwable throwable) {
            JOptionPane.showMessageDialog(null, new Object[]{
                "Terjadi error di database dengan pesan ", throwable.getMessage()});
        }

    }//GEN-LAST:event_buttonAddActionPerformed

