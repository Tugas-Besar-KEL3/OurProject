    public void tampil_combo() {
        try {
            Connection con = medikacomDatabase.getConnection();
            Statement stt = con.createStatement();
            String sql = "select idpengajar from data_pengajar order by idpengajar asc";
            ResultSet res = stt.executeQuery(sql);

            while (res.next()) {
                Object[] ob = new Object[3];
                ob[0] = res.getString(1);

                cbIdPengajar.addItem((String) ob[0]);
            }
            res.close();
            stt.close();
        } catch (Exception e) {
            System.out.println(e.getMessage());
        }
    }

    public void datatable() {
        DefaultTableModel tbl = new DefaultTableModel();
        tbl.addColumn("Kode mapel");
        tbl.addColumn("ID pengajar");
        tbl.addColumn("Mapel");
        tbl.addColumn("Prog. Keahlian");
        tbl.addColumn("Kurikulum");
        tblMapel.setModel(tbl);
        try {
            Statement statement = (Statement) medikacomDatabase.getConnection().createStatement();
            ResultSet res = statement.executeQuery("select*from data_mapel");
            while (res.next()) {
                tbl.addRow(new Object[]{
                    res.getString("kodemapel"),
                    res.getString("idpengajar"),
                    res.getString("mapel"),
                    res.getString("pkeahlian"),
                    res.getString("kurikulum")
                });
                tblMapel.setModel(tbl);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, "salah");
        }
    }

