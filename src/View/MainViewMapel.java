    private void btnLihatActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLihatActionPerformed
        // TODO add your handling code here:
        try {
            Object[] rows = {"Kode mapel", "ID pengajar", "Mapel", "Prog. Keahlian", "Kurikulum"};
            DefaultTableModel dtm = new DefaultTableModel(null, rows);
            tblMapel.setModel(dtm);
            tblMapel.setBorder(null);
            jScrollPane4.setVisible(true);
            jScrollPane4.setViewportView(tblMapel);
            String kodemapel = "", idpengajar = "", mapel = "", pkeahlian = "", kurikulum = "";
            try {
                String sql = "select * from data_mapel";
                Connection conn = medikacomDatabase.getConnection();
                Statement st = conn.createStatement();
                ResultSet rs = st.executeQuery(sql);
                while (rs.next()) {
                    kodemapel = rs.getString("kodemapel");
                    idpengajar = rs.getString("idpengajar");
                    mapel = rs.getString("mapel");
                    pkeahlian = rs.getString("pkeahlian");
                    kurikulum = rs.getString("kurikulum");

                    String[] tampil = {"" + kodemapel, idpengajar, mapel, pkeahlian, kurikulum};
                    dtm.addRow(tampil);
                }
            } catch (SQLException e) {
                e.printStackTrace();
                JOptionPane.showMessageDialog(null, "Query Salah " + e);
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnLihatActionPerformed

    private void buttonDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonDeleteActionPerformed
        // TODO add your handling code here:
        int ok = JOptionPane.showConfirmDialog(null, "Apakah anda yakin akan menghapus data ini?", "Confirmation", JOptionPane.YES_NO_OPTION);
        if (ok == 0) {
            try {
                String sql = "delete from data_mapel where kodemapel='" + txtKodeMapel.getText() + "'";
                Connection conn = medikacomDatabase.getConnection();
                PreparedStatement st = conn.prepareStatement(sql);
                st.executeUpdate();
                JOptionPane.showMessageDialog(null, "Hapus data sukses!");
            } catch (Throwable throwable) {
                JOptionPane.showMessageDialog(null, new Object[]{
                    "Terjadi error di database dengan pesan ", throwable.getMessage()});
            }
        }
    }//GEN-LAST:event_buttonDeleteActionPerformed

