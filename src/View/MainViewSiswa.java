    private void btnEdit1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEdit1ActionPerformed
        // TODO add your handling code here:
        int ok = JOptionPane.showConfirmDialog(null, "Apakah anda yakin akan mengupdate data ini?", "Confirmation", JOptionPane.YES_NO_OPTION);
        try {
            String sql = "update data_siswa set nis=?, nama=?, kelas=?, jk=?, ttl=?, alamat=?, nohp=? where nis='" + txtNisSiswa.getText() + "'";
            Connection conn = medikacomDatabase.getConnection();
            PreparedStatement st = conn.prepareStatement(sql);
            if (ok == 0) {
                try {
                    st.setString(1, txtNisSiswa.getText());
                    st.setString(2, txtNamaSiswa.getText());
                    st.setString(3, txtKelasSiswa.getText());
                    st.setString(4, cbJK.getSelectedItem().toString());
                    st.setString(5, txtTtlSiswa.getText());
                    st.setString(6, txtAlamatSiswa.getText());
                    st.setString(7, txtNoTelpSiswa.getText());

                    st.executeUpdate();

                    JOptionPane.showMessageDialog(null, "Update data sukses!");
                } catch (Throwable throwable) {
                    JOptionPane.showMessageDialog(null, new Object[]{
                        "Terjadi error di database dengan pesan ", throwable.getMessage()});
                }
            }
        } catch (Exception e) {

        }
    }//GEN-LAST:event_btnEdit1ActionPerformed

