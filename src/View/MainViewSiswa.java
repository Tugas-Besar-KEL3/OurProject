    public void datatable() {
        DefaultTableModel tbl = new DefaultTableModel();
        tbl.addColumn("NIS");
        tbl.addColumn("Nama");
        tbl.addColumn("Kelas");
        tbl.addColumn("Jk");
        tbl.addColumn("Ttl");
        tbl.addColumn("Alamat");
        tbl.addColumn("No. Hp");
        tblSiswa.setModel(tbl);
        try {
            Statement statement = (Statement) medikacomDatabase.getConnection().createStatement();
            ResultSet res = statement.executeQuery("select*from data_siswa");
            while (res.next()) {
                tbl.addRow(new Object[]{
                    res.getString("nis"),
                    res.getString("nama"),
                    res.getString("kelas"),
                    res.getString("jk"),
                    res.getString("ttl"),
                    res.getString("alamat"),
                    res.getString("nohp")
                });
                tblSiswa.setModel(tbl);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, "salah");
        }
    }

