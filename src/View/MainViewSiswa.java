    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        Integer nis = Integer.parseInt(txtNisSiswa.getText());
        String nama = txtNamaSiswa.getText();
        String kelas = txtKelasSiswa.getText();
        String jk = cbJK.getSelectedItem().toString();
        String ttl = txtTtlSiswa.getText();
        String alamat = txtAlamatSiswa.getText();
        Integer nohp = Integer.parseInt(txtNoTelpSiswa.getText());
        try {
            Statement statement = (Statement) medikacomDatabase.getConnection().createStatement();
            statement.executeUpdate("insert into data_siswa values ('" + nis + "','" + nama + "','" + kelas + "','" + jk + "','" + ttl + "','" + alamat + "','" + nohp + "');");
            statement.close();
            JOptionPane.showMessageDialog(null, "Data berhasil disimpan!");
        } catch (Throwable throwable) {
            JOptionPane.showMessageDialog(null, new Object[]{
                "Terjadi error di database dengan pesan ", throwable.getMessage()});
        }

    }//GEN-LAST:event_btnAddActionPerformed

    private void btnLihatActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLihatActionPerformed
        // TODO add your handling code here:
        try {
            Object[] rows = {"NIS", "Nama", "Kelas", "JK", "Ttl", "Alamat", "No. Hp"};
            DefaultTableModel dtm = new DefaultTableModel(null, rows);
            tblSiswa.setModel(dtm);
            tblSiswa.setBorder(null);
            jScrollPane1.setVisible(true);
            jScrollPane1.setViewportView(tblSiswa);
            int nis = 1;
            String nama = "", kelas = "", jk = "", ttl = "", alamat = "", nohp = "";
            try {
                String sql = "select * from data_siswa";
                Connection conn = medikacomDatabase.getConnection();
                Statement st = conn.createStatement();
                ResultSet rs = st.executeQuery(sql);
                while (rs.next()) {
                    nis = rs.getInt("nis");
                    nama = rs.getString("nama");
                    kelas = rs.getString("kelas");
                    jk = rs.getString("jk");
                    ttl = rs.getString("ttl");
                    alamat = rs.getString("alamat");
                    nohp = rs.getString("nohp");

                    String[] tampil = {"" + nis++, nama, kelas, jk, ttl, alamat, nohp};
                    dtm.addRow(tampil);
                }
            } catch (SQLException e) {
                e.printStackTrace();
                JOptionPane.showMessageDialog(null, "Query Salah " + e);
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnLihatActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        int ok = JOptionPane.showConfirmDialog(null, "Apakah anda yakin akan menghapus data ini?", "Confirmation", JOptionPane.YES_NO_OPTION);
        if (ok == 0) {
            try {
                String sql = "delete from data_siswa where nis='" + txtNisSiswa.getText() + "'";
                Connection conn = medikacomDatabase.getConnection();
                PreparedStatement st = conn.prepareStatement(sql);
                st.executeUpdate();
                JOptionPane.showMessageDialog(null, "Hapus data sukses!");
            } catch (Throwable throwable) {
                JOptionPane.showMessageDialog(null, new Object[]{
                    "Terjadi error di database dengan pesan ", throwable.getMessage()});
            }
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

